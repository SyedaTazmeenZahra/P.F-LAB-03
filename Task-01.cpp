//Working with different data types
#include <iostream>
using namespace std;
int main() {
    int digit = 10;
    float decimal = 5.7;
    char letter = 'T';
    bool t_f = true;

    cout << "Integer is: " << digit << '\n';
    cout << "Decimal is: " << decimal << '\n';
    cout << "Character is: " << letter << '\n';
    cout << "Boolean is: " << t_f << '\n';
    return 0;
}